package se;

public class Se {

	private int seID;
	private String seTitle;
	private String userID;
	private String seDate;
	private String seContent;
	private int seAvailable;
	
	public int getSeID() {
		return seID;
	}
	public void setSeID(int seID) {
		this.seID = seID;
	}
	public String getSeTitle() {
		return seTitle;
	}
	public void setSeTitle(String seTitle) {
		this.seTitle = seTitle;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getSeDate() {
		return seDate;
	}
	public void setSeDate(String seDate) {
		this.seDate = seDate;
	}
	public String getSeContent() {
		return seContent;
	}
	public void setSeContent(String seContent) {
		this.seContent = seContent;
	}
	public int getSeAvailable() {
		return seAvailable;
	}
	public void setSeAvailable(int seAvailable) {
		this.seAvailable = seAvailable;
	}
	
	
	
	
}
